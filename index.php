	<P> <b> <h1> <center>  KEBUDAYAAN ASLI INDONESIA  <center> </h1> </b> </P>
		
		
		<center> <img src="wayan01.jpg" alt="foto_saya" height="160" width="148"> </center>
		
		<P> Indonesia memiliki banyak beraneka ragam kebudayaan yaitu kebudayaan etnik dan kebudayaan asing, sedangkan Kebudayaan Nasional Indonesia sejak sumpah Pemuda, atau sejak Indonesia merdeka, sehingga kebudayaan yang ada sangat perlu dilestarikan dan dikembangkan oleh generasi muda saat ini, agar kekayaan kebudayaan yang dimiliki Indonesia selalu terlihat dan dipandang oleh negara lain bahwa betapa banyaknya kesenian budaya yang telah dilestarikan dan dibudayakan oleh bangsa kita.
		Sebagai contoh dari adanya kebudayaan di Indonesia diantaranya seperti kesenian Batik, kesenian Reog, dan kesenian Tari Pendet. Kebudayaan tersebut sangat khas dan terkenal di Indonesia. Bangsa yang maju adalah bangsa yang menghargai dan bangga akan kebudayaannya sendiri. Dari kebudayaan suatu bangsa dapat dilihat dari kemajuan dan Intelektualitas masyarakatnya. Indonesia sebagai bangsa yang plural dengan ragam kebudayaannya mampu menarik perhatian dunia salah satu warisan budaya tersebut adalah batik. Kesenian batik merupakan seni membuat motif desain berupa gambar di atas kain untuk pakaian yang menjadi salah satu kebudayaan keluarga raja-raja Indonesia zaman dulu.
		Batik yang merupakan budaya asli bangsa Indonesia adalah salah satu kekayaan budaya bangsa yang harus dilestarikan dan dikembangkan terus menerus yang menyimpan berbagai kearifan yang mengakar secara substansial dari sisi ornamentasi keselarasan, proses pembuatannya, hingga cara mengapresiasikannya, keunikan, motif, serta corak yang dihasilkan dari batik-batik di berbagai daerah merupakan kekuatan yang sangat luar biasa khususnya bagi kekayaan seni budaya Indonesia dan belum ada di negara manapun yang memiliki kekayaan rancangan motif yang unik pada batik seperti yang dimiliki bangsa Indonesia.
		
